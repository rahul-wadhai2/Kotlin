package com.jejecomms.realtimechatfeature.data.model

/**
 * Enum to represent the type of a chat message.
 */
enum class MessageType {
    /**
     *  Message type for text messages.
     */
    TEXT,

    /**
     *  Message type for image messages.
     */
    IMAGE,

    /**
     *  Message type for Pdf messages.
     */
    DOCUMENT,

    /**
     * Message type for audio messages.
     */
    AUDIO
}